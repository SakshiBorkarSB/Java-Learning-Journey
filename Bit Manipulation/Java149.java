// Add 1 to an integer using Bit Manipulation.
// (Hint : try using Bitwise NOT Operator)

public class Java149 {
    public static void main(String args[]){
        int num = 6;
        System.out.println(num + " + 1 = " + -~num);

        num = -4;
        System.out.println(num + " + 1 = " + -~num);
    }    
}
