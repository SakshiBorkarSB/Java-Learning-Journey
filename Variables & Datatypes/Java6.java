// Product of two numbers

import java.util.*;
public class Java6{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the two numbers");
        float a =  sc.nextFloat();
        float b =  sc.nextFloat();
        float product = a * b;
        System.out.println(product);
    }
}
