// Arithmetic Operators in Java
// BINARY OPERATORS

import java.util.*;
public class Java15{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);

        int a = 10;
        int b = 5;

        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));
        System.out.println("a * b = " + (a*b));
        System.out.println("a / b = " + (a/b));
        System.out.println("a % b = " + (a%b));
    }
}
