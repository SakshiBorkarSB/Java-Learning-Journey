// Assignment Operator

import java.util.*;
public class Java19{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int a = 7;
        System.out.println("a + 2 = " + (a+=2));
        System.out.println("a - 2 = " + (a-=2));
        System.out.println("a * 2 = " + (a*=2));
        System.out.println("a / 2 = " + (a/=2));
        System.out.println("a % 2 = " + (a%=2));
    }
}
